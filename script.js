// Animated text cycling through developer roles
function initAnimatedText() {
    const animatedElement = document.getElementById('animated-text');
    const roles = [
        'Frontend Developer',
        'Java Developer', 
        'Fullstack Developer',
        'Software Engineer',
        'Web Developer'
    ];
    
    let currentRoleIndex = 0;
    let currentCharIndex = 0;
    let isDeleting = false;
    let isPaused = false;
    
    function typeAnimation() {
        const currentRole = roles[currentRoleIndex];
        
        if (!isDeleting && currentCharIndex < currentRole.length) {
            // Typing
            animatedElement.textContent = currentRole.substring(0, currentCharIndex + 1);
            currentCharIndex++;
            setTimeout(typeAnimation, 100);
        } else if (isDeleting && currentCharIndex > 0) {
            // Deleting
            animatedElement.textContent = currentRole.substring(0, currentCharIndex - 1);
            currentCharIndex--;
            setTimeout(typeAnimation, 50);
        } else if (!isDeleting && currentCharIndex === currentRole.length) {
            // Pause before deleting
            if (!isPaused) {
                isPaused = true;
                setTimeout(() => {
                    isPaused = false;
                    isDeleting = true;
                    typeAnimation();
                }, 2000);
            }
        } else if (isDeleting && currentCharIndex === 0) {
            // Move to next role
            isDeleting = false;
            currentRoleIndex = (currentRoleIndex + 1) % roles.length;
            setTimeout(typeAnimation, 500);
        }
    }
    
    // Start the animation
    typeAnimation();
}

// Initialize animated text when page loads
window.addEventListener('load', () => {
    // Add a small delay before starting the animation
    setTimeout(() => {
        initAnimatedText();
    }, 1000);
});

